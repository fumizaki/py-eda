def instruct_freqtable() -> str:
    lang = {
        'JA': {
            'instruction': """
        **目的:**
        - 単一の質的変数（カテゴリカル変数）に含まれる各カテゴリの出現割合（相対度数）、累積的な出現割合（累積相対度数）、および参考として度数を数値で示します。これにより、カテゴリの分布を全体の中で捉え、累積的な影響を評価することを目的とします。

        **示唆:**
        - **カテゴリの相対的な重要性:** 各カテゴリがデータ全体に占める割合を直接的に比較できます。
        - **累積的な構成比:** 特定のカテゴリまでのデータが全体の中でどの程度の割合を占めるかを知ることができます。
        - **上位カテゴリの貢献度:** 相対度数の高いカテゴリが、累積相対度数にどれほど寄与しているかを確認できます。
        - **特定の閾値を超えるカテゴリの特定:** 累積相対度数を見ることで、上位何位までのカテゴリでデータ全体の一定割合（例：80%）を占めているかを特定できます。
        - **意思決定への応用:** 累積相対度数は、リソース配分や優先順位付けなど、データに基づいた意思決定を支援します。
        """
        }
    }

    return lang['JA']['instruction']