def instruct_corrmatrix() -> str:
    lang = {
        'JA': {
            'instruction': """
        **目的:**
        - 選択された数値変数間の線形相関の強さと方向を視覚化します。
                 
        **示唆:**
        - 色の濃淡で相関係数の絶対値の大きさ（相関の強さ）を示します。暖色系は正の相関、寒色系は負の相関を示すことが多いです（カラーマップによります）。
        - 対角成分は常に1（自身との相関）です。
        - 強い正または負の相関（1または-1に近い値）を持つ変数ペアは、一方が変化すると他方も予測可能な形で変化する傾向があることを示唆します。
        - 0に近い値は、変数間に線形な関係がほとんどないことを示唆します。
        - 多重共線性（ multicollinearity）の特定に役立ちます（非常に相関の高い変数群がないか確認）。
        """
        }
    }

    return lang['JA']['instruction']